angular.module('manager').config(function($routeProvider){
    $routeProvider.when('/',{
        templateUrl:'pages/mainPage/mainPage.html'
    })
    .when('/customerList',{
        templateUrl:'pages/customerList/customerList.html'
    })
    .when('/staffList',{
        templateUrl:'pages/staffList/staffList.html'
    })
    .when('/addCustomer',{
        templateUrl:'pages/addCustomer/addCustomer.html'
    });
    $routeProvider.otherwise('/');
});
